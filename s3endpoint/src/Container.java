
public class Container {
	private String _bucketName;
	private String _objectKey;
	private String _objectSize;
	private String _objectEtag;
	private String _eventTime;
	private String _eventName;
	public String get_bucketName() {
		return _bucketName;
	}
	public void set_bucketName(String _bucketName) {
		this._bucketName = _bucketName;
	}
	public String get_objectKey() {
		return _objectKey;
	}
	public void set_objectKey(String _objectKey) {
		this._objectKey = _objectKey;
	}
	public String get_objectSize() {
		return _objectSize;
	}
	public void set_objectSize(String _objectSize) {
		this._objectSize = _objectSize;
	}
	public String get_objectEtag() {
		return _objectEtag;
	}
	public void set_objectEtag(String _objectEtag) {
		this._objectEtag = _objectEtag;
	}
	public String get_eventTime() {
		return _eventTime;
	}
	public void set_eventTime(String _eventTime) {
		this._eventTime = _eventTime;
	}
	public String get_eventName() {
		return _eventName;
	}
	public void set_eventName(String _eventName) {
		this._eventName = _eventName;
	}
}

